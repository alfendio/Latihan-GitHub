# membuat segitiga siku-siku
# input: alas dan tinggi
# output: luas segitiga

alas = int(input("Masukkan alas: "))
tinggi = int(input("Masukkan tinggi: "))
luas = alas * tinggi / 2
print("Luas segitiga: ", luas)

